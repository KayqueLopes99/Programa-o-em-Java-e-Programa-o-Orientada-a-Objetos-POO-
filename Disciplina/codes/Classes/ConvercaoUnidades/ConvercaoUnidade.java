package Disciplina.codes.Classes.ConvercaoUnidades;

public class ConvercaoUnidade {
    public static double polegadasParaCentimetros(double polegadas){
        return polegadas * 2.54;

    }

    public static double pesParaCentimetros(double pes) {
        return pes * 30.48;
    }

    public static double milhasParaQuilometos(double milhas){
        return milhas * 1.609;
    }

}
