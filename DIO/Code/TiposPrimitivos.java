package DIO.Code;

public class TiposPrimitivos {
    public static void main(String[] args) {
        int idade = 20;
        long numero_grande = 1000000L;
        float salario_float = 2000F;
        double salario_double = 2500;
        String caracteres = "Kayque Lopes";
        char caractere = 'K';
        boolean verdade = true;

        System.out.println("Tipo inteiro: " + idade);
        System.out.println("Tipo long: " + numero_grande);
        System.out.println("Tipo float: " + salario_float);
        System.out.println("Tipo String: " + caracteres);
        System.out.println("Tipo Char: " + caractere);
        System.out.println("Tipo Boleano: " + verdade);
        System.out.println("Tipo Double: " + salario_double);
        
    }
    
}
