package Udemy.Code.Array_arraylist;

public class DatePerson {
    private double height;
    private String gender;
    
    public DatePerson(double height, String gender){
        this.height = height;
        this.gender = gender;
    }

    public String getGender() {
        return gender;
    }

    public double getHeight() {
        return height;
    }

    
}
